        if e is not None:
            x = torch.cat([x, e], 1)
        x = self.bn(x)
        x = F.relu(self.conv1(x), inplace=True)
        x = F.relu(self.conv2(x), inplace=True)
        g1 = self.spatial_gate(x)
        g2 = self.channel_gate(x)
        x = x * g1 + x * g2
        return x


